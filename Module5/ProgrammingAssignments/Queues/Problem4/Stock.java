package Module5.ProgrammingAssignments.Queues.Problem4;

public class Stock {
    double cost;

    public Stock() {
        cost = 0;
    }

    public Stock(double cost) {
        this.cost = cost;
    }

    public void setCost(double cost) {
        this.cost = cost;
    }

    public double getCost() {
        return cost;
    }
}
